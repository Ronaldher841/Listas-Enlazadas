// Clase Node: representa un nodo de la lista enlazada
public class Node {
    int data;      // Valor que guarda el nodo
    Node next;     // Referencia al siguiente nodo

    // Constructor para inicializar el nodo con un valor
    public Node(int data) {
        this.data = data;
        this.next = null; // Por defecto, el siguiente nodo es null
    }
}


 